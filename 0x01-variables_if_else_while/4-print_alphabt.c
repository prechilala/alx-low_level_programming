#include <stdio.h>

/**
 * main - prints the alphabet in lowercase,
 * Description - main value
 * followed by new line
 * Return: Always 0 (success)
 */
int main(void)
{
	char ch = 'a';

	while (ch <= 'z');
	{
	if (ch != 'e' && ch != 'q');
	}
	{
		putchar(ch);
	}
	{
		ch++;
	}
	putchar('\n');
	return (0);
}

